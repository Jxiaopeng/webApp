angular.module("appRouteCtrl",[])
    .config(function($stateProvider,$urlRouterProvider){
        $urlRouterProvider.otherwise("/tab/chat");
        $stateProvider
            .state(
                "tab",{
                    url:"/tab",
                    abstract: true,
                    templateUrl:"templates/tab.html"
                })
            .state(
                "tab.dash", {
                    url:"/dash",
                    views:{
                        "tab-dash": {
                            templateUrl:"templates/tab-dash.html",
                            controller:"dashCtrl"
                        }
                    }
                })
            .state(
                "tab.chat",{
                    url:"/chat",
                    views:{
                        "tab-chat":{
                            templateUrl:"templates/tab-chat.html",
                            controller:"chatCtrl"
                        }
                    }
                })
            .state(
                "tab.chat-detail",{
                    url:"/chat/:chatId",
                    views:{
                        "tab-chat":{
                            templateUrl:"templates/chat-detail.html",
                            controller:"chatDetailCtrl"
                        }
                    }
                })
            .state(
                "tab.frient",{
                    url:"/friend",
                    views:{
                        "tab-friend":{
                            templateUrl:"templates/tab-friend.html",
                            controller:"friendCtrl"
                        }
                    }
                })
            .state(
                "tab.frient-detail",{
                    url:"/friend/:friendId",
                    views:{
                        "tab-friend":{
                            templateUrl:"templates/friend-detail.html",
                            controller:"friendDetailCtrl"
                        }
                    }
                })
            .state("tab.account",{
                url:"/account",
                views:{
                    "tab-account":{
                        templateUrl:"templates/tab-account.html",
                        controller:"accountCtrl"
                    }
                }
            })

    })